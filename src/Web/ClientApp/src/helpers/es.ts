export function LANG(property: string) {
    switch (property) {
        case "name":
            return "Nombre";
        case "flowerId":
            return "Flor";
        case "flower":
            return "Flor";
        case "preferredAddress":
            return "Dirección preferida";
        case "phoneNumber":
            return "Teléfono";
        case "preferredCommunicationTypeId":
            return "Tipo de comunicación preferido";
        case "link":
            return "Enlace";
        case "wrapperId":
            return "Envoltura";
    }
}
